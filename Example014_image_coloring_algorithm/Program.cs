void FillImage(int row, int col)  // создали метод лоя закрашивания, и в качестве аргумента укзываем позицию строки (row) и пикселя (col) с которого мы хотим начать закрашивать картинку.
{
    if(pic[row, col] == 0) // если наш текуший пиксель равен 0, те он не закрашен
    {
        pic[row, col] = 1;  // то мы этот пиксель красим (+) из кода выше
        // Вызываем метод в методе (РЕКУРСИЯ!!!!) и прописываем алгоритм по котрому будет происходить закрашивание
        FillImage(row - 1, col); // от местонахождения: строка ВВЕРХ!, столбец на месте
        FillImage(row, col - 1); // строка на месте, столбец ВЛЕВО!
        FillImage(row + 1, col); // строка ВНИЗ!, столбец на месте
        FillImage(row, col + 1); // строка на месте, столбец ВПРАВО!
    }
}

